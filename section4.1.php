				<h2>During program implementation</h2>
				<ul class="document-list">
					<li>Complete the fidelity monitoring tool as activities are implemented – It is important to update the fidelity monitoring tool soon after activities are implemented. Delays between implementation and documentation can make it difficult to remember details. For example, a staff member may forget if a particular handout was provided to participants. Likewise, a staff member may forget that a particular video couldn’t be shown to participants because the DVD player was not working.</li>
					<li>Document any implementation problems that arise – The identification of problems will help program planners develop strategies to avoid these same problems in the future. Additionally, program evaluators can use documentation of implementation problems to help explain disparities between their program outcomes and those seen during evaluation studies on the program.</li>
					<li>Provide ongoing training and support – Program staff can work together to make sure each staff member understands and is completing his or her role in fidelity monitoring. If some fidelity monitoring activity is not working as planned, then staff can address this and make changes in a timely manner so it will work for the remainder of the program.</li>
				</ul>
				<h2>After program implementation</h2>
				<ul class="document-list">
					<li>Review fidelity monitoring tools for completion – Make sure that all of the forms used to monitor fidelity have been completed.</li>
					<li>Work with program evaluators to determine the extent of program fidelity and if this influenced outcomes – Program evaluators can consider if deviations from a program’s protocol, such as sessions that were not offered or materials that were not provided, influenced program outcomes.</li>
					<li>Use fidelity monitoring data to plan for future rounds of implementation – Strengths and trouble spots can be identified so strengths are built upon and trouble spots are targeted for improvement the next time the program is implemented. Fidelity monitoring data is also useful for quality assurance activities (see Quality Assurance for more details).</li>
				</ul>
				<p>Despite the need for fidelity, it is likely that your organization would like to change certain elements of the program you have chosen to implement. In fact, you may feel that these elements must be changed in order for the program to produce positive outcomes among your audience members. What do you do in these situations? The good news is that some program elements can be changed without jeopardizing fidelity. These are discussed in the following section.</p>
