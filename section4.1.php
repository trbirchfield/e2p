				<p>The steps to fidelity monitoring include activities that take place before, during, and after program implementation. Each staff member who participates in program implementation can also participate in fidelity monitoring. If program personnel are numerous, it may be useful to select a few of these individuals to form a team that carries out the bulk of the monitoring. Depending on the nature of the program, individuals other than program staff, such as the program’s developers, may also take part.</p>
				<h2>Before program implementation</h2>
				<ul class="document-list">
					<li>Identify and understand the key elements or components of the program – Read through the program curriculum and be acquainted with each topic to be addressed, as well as the worksheets, checklists, tip sheets, and other handouts that will be given to participants. Determine how knowledge and skills are built as the program advances.</li>
					<li>Learn about the theoretical basis of the program – Study the theory or theories of behavior change (e.g., Social Cognitive Theory, Stages of Change, Health Belief Model) that underpin the program. Working knowledge of the theoretical underpinnings will provide an understanding of how program components fit together and why they are essential.</li>
					<li>Develop a fidelity monitoring tool – If a fidelity monitoring tool was provided by the
					developers of your program, then this step is simplified for you. If a tool was not provided, then work collaboratively to develop an easy-to-complete tool. Checklists are frequently used as fidelity monitoring tools. At minimum, a fidelity checklist should contain a list of each key element or component of the program. These will be checked off once completed. Optimally, a checklist will also contain designated areas to document when each activity was completed as well as other relevant information such as who completed each activity and any contextual factors (e.g., tragedies within the community, programs implemented by other organizations) that might influence the impacts of the activities. Example fidelity checklists can be found in the <a href="http://patienteducation.stanford.edu/licensing/Fidelity_ToolKit2010.pdf" target="_blank">Stanford Self-Management Fidelity Toolkit</a>.</li>
					<li>Provide fidelity monitoring training – Training opportunities will make sure each staff member responsible for fidelity monitoring understands the significance of fidelity and how to use the fidelity monitoring tool.</li>
				</ul>
