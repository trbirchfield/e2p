				<p>EBPs require a financial commitment from implementing organizations. Some programs can be implemented for a few hundred dollars, while some programs require several thousand dollars for successful implementation. The costs of developing and evaluating an EBP can be significant, so program developers will often collect licensing fees or similar charges in order to recover research and development costs or cover the costs associated with ongoing administration of a program. Several potential funding sources for EBPs are discussed below.</p>
				<ul class="document-list">
					<li><em>Existing funds</em> – Some organizations can use financial resources they already have to fund a new EBP. For example, the funding used to support a program that is ending could be reallocated to fund the new program.</li>
					<li><em>Charitable organizations and foundations</em> – Many national, regional, and local charitable organizations and foundations offer grants. It may be especially beneficial to seek funding from groups that serve specific populations (e.g., senior adults, particular ethnic groups, individuals with specific diseases).</li>
					<li><em>Healthcare organizations</em> – Some healthcare organizations, including non-profit hospitals, private physicians’ practices, managed care organizations, and Federally Qualified Health Centers, offer financial and in-kind support to community organizations that implement EBPs. In general, it is useful to demonstrate how an EBP will save healthcare dollars, reduce healthcare utilization, attract and retain clients, and generate social capital when applying for funding from healthcare organizations.</li>
					<li><em>Medicaid</em> – In some states, organizations can be reimbursed through Medicaid waivers or Medicaid state plans for Medicaid patients’ participation in EBPs. Currently, this occurs on a small scale, but it is becoming more common. Reimbursement opportunities vary from state-to-state, so details should be sought from your respective state Medicaid office. In Texas, organizations currently cannot be reimbursed by Medicaid. Many senior health advocates are working to change this.</li>
					<li><em>Advocacy</em> – By advocating among city, county, and state lawmakers, organizations are sometimes able to increase the funding that is available for health promotion and disease prevention programs.</li>
					<li><em>Senior housing</em> – Assisted living facilities, Naturally Occurring Retirement Communities, and HUD-funded facilities are sometimes willing to provide funding for the provision of EBPs for their residents. At minimum, these facilities can provide in-kind facilities for program sessions.</li>
					<li><em>Participants</em> – Organizations can request donations from participants or charge fees for program participation. Some organizations balk at the thought of charging participants to participate in a health promotion program; however, charging participants may actually be beneficial in increasing the perceived value of the program and increasing participants’ commitment to complete the program. Fees can be determined on a sliding scale to encourage people of all income levels to participate. An alternative is charging participants for certain components of a program, such as workbooks, program t-shirts, or other supplies.</li>
				</ul>
