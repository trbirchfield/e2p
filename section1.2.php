				<p>Though there are numerous advantages to implementing EBPs, there are also a number of challenges. Several of these are discussed below.</p>
				<ul class="document-list">
					<li><em>Match with target audience</em> – Many EBPs were developed for and evaluated after implementation within a particular target audience (e.g., seniors with depression, heart attack survivors, assisted living residents). While positive outcomes were found by researchers among these particular audiences, the programs may not produce similar results among other audiences.</li>
					<li><em>Limited adaptability</em> – EBPs are intended to be implemented as designed <span class="reference">(see <a href="/section4.1.php">What is Fidelity?</a>)</span>. Thus, there is often limited room to adapt or customize programs to local audiences.</li>
					<li><em>Cost</em> – Many EBPs are copyrighted, which means you must gain permission from the developers or purchase licenses to implement the programs. You may also need to purchase manuals or workbooks and your staff may need to attend training sessions. The expenses associated with licenses, materials, and trainings may add up, making a program quite costly to implement. Moreover, the researchers who developed the programs may have provided participant incentives (e.g., t-shirts, gift cards) to encourage participation in the programs. If your organization cannot provide these incentives, you may have a more difficult time recruiting and retaining participants.</li>
					<li><em>Exclusion of beneficial programs due to lack of empirical research</em> – Restricting program offerings to those that are evidence-based may mean some programs that produce positive outcomes are excluded simply because they have not been researched.</li>
					<li><em>Unknown long-term effects</em> – There is no research on the long-term effects of many EBPs. Therefore, it is unknown if these programs produce benefits 5 or 10 years after their implementation.</li>
				</ul>
